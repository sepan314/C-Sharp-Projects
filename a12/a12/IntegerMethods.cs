using System;
namespace a12
{
    public class IntegerMethods
    {
        public static int Add1000(int integer)
        {
            return integer += 1000;
        }

        public static int Multiply3(int integer)
        {
            return integer *= 3;
        }

        public static int Square(int integer)
        {
            return integer *= integer;
        }
    }
}
